        if (optIngeniería.isSelected()) {
            carrera = "Igeniería en Informática";
        }
        if (optTécnico.isSelected()) {
            carrera = "TU en Informática";
        }
        JOptionPane.showMessageDialog(rootPane, carrera);
        
        
