    private void btnComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComboActionPerformed
        int index = cboDias.getSelectedIndex();
        String dia = cboDias.getSelectedItem().toString();

        if (index == 0) {
            JOptionPane.showMessageDialog(rootPane,
                    "Debes seleccionar un día",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        } else {

            JOptionPane.showMessageDialog(rootPane,
                    "Seleccionado día " + dia,
                    "Index " + index,
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnComboActionPerformed

